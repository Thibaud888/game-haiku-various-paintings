// URL Wikimedia (utilisée comme fallback si le téléchargement local n'a pas été fait).
function wikiImg(filename) {
  return 'https://commons.wikimedia.org/wiki/Special:FilePath/'
       + encodeURIComponent(filename) + '?width=800';
}

